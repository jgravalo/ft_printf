int	ft_isalpha(int c)
{
	if ((c >= 'A') && (c <= 'Z'))
	{
		return (2);
	}
	else if ((c >= 'a') && (c <= 'z'))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
